// battleanimobj: MACRO
// 	db \1 ; flags
// 	; bit 7: priority
// 	; bit 6: y flip (for enemy)
// 	; bit 5: x flip (for enemy)
// 	; bit 0: enable enemy animation coord fixing (x = 0xb4 - x; see below for y)
// 	db \2 ; enemy animation y fix param
// 	; if 0xFF: y = y + 5 tiles
// 	; else:   y = -y - (1 tile) * (is_softboiled_animation)
// 	db \3 ; video sequence
// 	db \4 ; callback
// 	db \5 ; palette
// 	db \6 ; tile offset
// ENDM
#define battleanimobj(_flags, _anim, _seq, _callback, _pal, _offset) {_flags, _anim, _seq, _callback, _pal, _offset}

